import * as React from "react";
import { cn } from "@/lib/utils/cn";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { variant?: "default" | "glass" | "elevated" }
>(({ className, variant = "default", ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border text-card-foreground transition-colors",
      variant === "default" && "bg-card border-border/60",
      variant === "glass" && "glass",
      variant === "elevated" && "bg-card-elevated border-border/60 shadow-soft",
      className,
    )}
    {...props}
  />
));
Card.displayName = "Card";

