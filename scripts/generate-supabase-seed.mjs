function uuidFor(key) {
  const hex = createHash("sha1").update(`nexus-seed:${key}`).digest("hex").slice(0, 32);
  return `${hex.slice(0, 8)}-${hex.slice(8, 12)}-5${hex.slice(13, 16)}-a${hex.slice(17, 20)}-${hex.slice(20, 32)}`;
}

function q(value) {
  if (value === null || value === undefined) {
    return "null";
  }
  return `'${String(value).replace(/'/g, "''")}'`;
}

function qJson(value) {
  if (value === null || value === undefined) {
    return "null";
  }
  return `${q(JSON.stringify(value))}::jsonb`;
}

function slugify(value) {
  return value
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function workspaceLookup(workspaceId) {
  const workspace = workspaceById.get(workspaceId);
  return `(select id from public.workspaces where slug = ${q(workspace.slug)})`;
}

function userLookup(userId) {
  const user = userById.get(userId);
  return `(select id from public.users where email = ${q(user.email)})`;
}

